"""
GraphRAG Service
================
Wrapper around Microsoft GraphRAG 0.3.6.

Responsibilities:
  - Prepare a per-document working directory with settings.yaml
  - Run the GraphRAG indexing pipeline as a subprocess
  - Parse parquet output into typed Python dataclasses
  - Build and run GlobalSearch (Discover mode)
  - Build and run LocalSearch (Connect / Challenge mode)

What this file does NOT do:
  - Write to Neo4j  → caller (Lambda handler) does that
  - Know about HTTP  → routers handle that
  - Load .env        → config.py handles that

Design decisions:
  - Per-document working directories   → safe for concurrent Lambda runs
  - Subprocess for indexing            → stable API; GraphRAG CLI is the
                                         intended entry point for indexing
  - GraphRAG Python API for querying   → gives typed SearchResult objects
                                         with richer metadata than CLI output
  - Singleton via module-level instance → one object shared across the app
"""

