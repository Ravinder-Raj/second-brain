# ── NVIDIA NIM endpoint (OpenAI-compatible) ──────────────────────────────────
# NIM exposes the same REST surface as OpenAI, so GraphRAG's ChatOpenAI client
# works by pointing api_base at NIM and setting api_type to OpenAI.
NVIDIA_NIM_BASE_URL = "https://integrate.api.nvidia.com/v1"
NVIDIA_NIM_MODEL    = "meta/llama-3.1-8b-instruct"

# NIM does not expose an embeddings endpoint (as of 0.3.6 integration).
# We use a lightweight OpenAI-compatible embedding via NIM's nvolveqa model.
NVIDIA_EMBED_MODEL  = "nvidia/nv-embedqa-e5-v5"

# GraphRAG community detection level — 0 is the coarsest (fastest), 1 is finer.
# Level 2 is available but slow for free-tier NIM.
COMMUNITY_LEVEL = 1


# ── Typed return types ────────────────────────────────────────────────────────

@dataclass
class ExtractedEntity:
    """One entity extracted from a document."""
    id:          str
    name:        str
    type:        str
    description: str
    community:   str | None = None  # community id this entity belongs to


@dataclass
class ExtractedRelationship:
    """One directional relationship between two entities."""
    id:          str
    source:      str   # entity name
    target:      str   # entity name
    type:        str
    description: str
    weight:      float = 1.0


@dataclass
class ExtractedCommunity:
    """One community (cluster of related entities) with its LLM summary."""
    id:      str
    title:   str
    summary: str
    level:   int


@dataclass
class IndexingResult:
    """
    Everything produced by run_indexing().
    The Lambda handler receives this and saves each list to Neo4j.
    """
    doc_id:         str
    entities:       list[ExtractedEntity]       = field(default_factory=list)
    relationships:  list[ExtractedRelationship] = field(default_factory=list)
    communities:    list[ExtractedCommunity]    = field(default_factory=list)


@dataclass
class SearchResult:
    """Returned by global_search() and local_search()."""
    response:       str
    entities_used:  list[str]   = field(default_factory=list)  # entity names touched
    completion_time: float      = 0.0
    llm_calls:      int         = 0
    prompt_tokens:  int         = 0


